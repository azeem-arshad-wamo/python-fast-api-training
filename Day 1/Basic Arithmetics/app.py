# Printing basic arithmetics
print(2+2) # Result: 4

print(50 - 32) # Result: 18

# Using "/" always returns a decimal
print(55 / 23) # 2.391304347826087

# Use "//" to floor the divisions
print(55 // 23) # Prints 2

# We can use "**" to calculate powers
print(5 ** 2)  # Prints 25

print(3 ** 8) # Prints 6561

